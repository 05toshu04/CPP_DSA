#include <iostream>
using namespace std;    
int main(){

// implicit conversion 
int a='c';
 
cout<<"Value of a: "<<a<<endl; 

int is_true=true;
cout<<"Value of is_true: "<<is_true<<endl;

// explicit conversion
float b=3.14;
int c=(int)b;
cout<<"Value of c: "<<c<<endl;

char d='a';
int e=(int)d;
cout<<"Value of e: "<<e<<endl;








}